#include<stdio.h>
#include<stdlib.h>

int main(){
    int x,y,z;


    // Pattern 1: Right Triangle
    for(x=1;x<=5;x++){
        for(y=1;y<=x;y++){
            printf("%d",x);
        }
        printf("\n");
    }
    
    // ------------------------------------------------------------------------------------------------------------------   
    // Pattern 2: Inverted Right Triangle
    for(x=5;x>=1;x--){
        for(y=1;y<=x;y++){
            printf("%d",x);
        }
        printf("\n");
    }
    
    
    // Pattern 3: Number Pyramid
    // ------------------------------------------------------------------------------------------------------------------   
    z=1;
    for(x=0;x<=4;x++){
        for(y=1;y<=9;y++){
            if(y>=5-x && y<=5+x)
            printf("%d",z);
            else
            printf(" ");
        }
        printf("\n");
        z++;
    }
    
    
    // ------------------------------------------------------------------------------------------------------------------   
    // Pattern 4: Number Square
    for(x=1;x<=5;x++){
        for(y=1;y<=5;y++){
            printf("%d",y);
        }
        printf("\n");
    }
    
    
    // ------------------------------------------------------------------------------------------------------------------   
    // Pattern 4: Number Square
    for(x=1;x<=5;x++){
        for(y=5;y>=1;y--){
            printf("%d",y);
        }
        printf("\n");
    }
    
       
    return 0;
}

